    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        if(password.getText().equals("")|| conf_password.getText().equals("")||aadhar_no.getText().equals(""))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(password.getText().equals(conf_password.getText()))
            {
                if(capp.changePass(password.getText(),aadhar_no.getText()))
                {
                    JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Password Changed Successfully......","Success",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    new Login().setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Error while updating password.....","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Password and Confirm Password are not same....","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_submit_btnActionPerformed

