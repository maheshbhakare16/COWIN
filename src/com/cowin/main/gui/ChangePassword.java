    private void otp_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_otp_btnActionPerformed
        // TODO add your handling code here:
        
        if(aadhar_no.getText().equals("")|| utype.getSelectedItem().equals("Select User Type"))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
             if(capp.getOtp(aadhar_no.getText(),(String)utype.getSelectedItem()))
             {
                 JOptionPane.showMessageDialog(new javax.swing.JFrame(),"OTP send Successfully Check your SMS.","Success",JOptionPane.INFORMATION_MESSAGE);
             }
             else
             {
                 JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Error While Sending OTP.\n Try after some time...","Error",JOptionPane.ERROR_MESSAGE);
             }
        }
    }//GEN-LAST:event_otp_btnActionPerformed

    private void validate_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validate_btnActionPerformed
        // TODO add your handling code here:
        if(otp.getText().equals(""))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(capp.validateOTP(otp.getText()))
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"OTP verified Successfully......","Success",JOptionPane.INFORMATION_MESSAGE);
                password.setVisible(true);
                conf_password.setVisible(true);
                password_txt.setVisible(true);
                conf_password_txt.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Wrong OTP.....","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_validate_btnActionPerformed

