    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        if(vac_date.getDate()==null|| vac_time.getSelectedItem().equals("Select Slot"))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(vapp.insertData(vac_date.getDate(),(String) vac_time.getSelectedItem()))
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Data Inserted Successfully...","SUCCESS",JOptionPane.INFORMATION_MESSAGE);
                vac_date.setDate(null);
                vac_time.setSelectedItem("Select Slot");
            }
            else
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Error While Inserting Data....","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_submit_btnActionPerformed

