    private void vaccinated_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vaccinated_btnActionPerformed
        // TODO add your handling code here:
        if(vaccine_name.getSelectedItem().equals("Select Vaccine Name"))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            int index = table_appointed.getSelectedRow();
            String aadhar_no = table_appointed.getValueAt(index, 3).toString();
            String date= table_appointed.getValueAt(index, 4).toString();
            String does1 = table_appointed.getValueAt(index, 7).toString();
            if(vapp.setVaccinatedReport((String)vaccine_name.getSelectedItem(),aadhar_no,does1,date))
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Successfully Updated Record...,","Success",JOptionPane.INFORMATION_MESSAGE);
                aadhar_no_find.setText("");
                DefaultTableModel t_model = ( DefaultTableModel)table_appointed.getModel();
                t_model.setRowCount(0);
            }
            else
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Error While Updating Record..\n Try after some time...","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_vaccinated_btnActionPerformed

