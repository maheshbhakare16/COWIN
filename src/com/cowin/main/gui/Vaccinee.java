    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        // TODO add your handling code here:
        if(select_date.getDate()==null|| select_slot.getSelectedItem().equals("Select Slot")||select_vaccinator.getSelectedItem().equals("Select Vaccinator"))
        {
            JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Please Enter all the fields Properly....","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(vapp.setAppointedData(select_date.getDate(),(String)select_slot.getSelectedItem(),(String)select_vaccinator.getSelectedItem()))
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Successfully Added Appointment... \nYou can login now","Success",JOptionPane.INFORMATION_MESSAGE);
                select_date.setDate(null);
                select_slot.removeAllItems();
                select_vaccinator.removeAllItems();
                select_slot.addItem("Select Slot");
                select_slot.setSelectedItem("Select Slot");
                select_vaccinator.addItem("Select Vaccinator");
                select_vaccinator.setSelectedItem("Select Vaccinator");
            }
            else
            {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"Error While Adding Appointment...\n Try after some time...","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_submit_btnActionPerformed

