    private void get_vaccinator_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_get_vaccinator_btnActionPerformed
        // TODO add your handling code here:
        if(select_date.getDate() == null || select_slot.getSelectedItem().equals("Select Slot"))
        {
            
        }
        else
        {
        List<String> l = vapp.getVaccinator(select_date.getDate(),(String)select_slot.getSelectedItem());
        select_vaccinator.removeAllItems();
        for(String s : l)
        {
            select_vaccinator.addItem(s);
        }
        select_vaccinator.setSelectedItem("Select Vaccinator");
        }
    }//GEN-LAST:event_get_vaccinator_btnActionPerformed

